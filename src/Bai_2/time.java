package Bai_2;

public class time {
    private String start ;
    private String end ;

    public String getStart() {
        return start;
    }

    public time(String start, String end) {
        this.start = start;
        this.end = end;
    }

    public void setStart(String start) {
        this.start = start;
    }

    public String getEnd() {
        return end;
    }

    public void setEnd(String end) {
        this.end = end;
    }
    public time(){}
}
